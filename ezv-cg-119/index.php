		<p>The vast open living space overlooks the beautiful garden and it features a gorgeous natural design that is undeniably stylish and contemporary at the same time. The places to go lounge and dine are abundant - enough space for everyone to find what they need: cool communal hangouts spots and places to find some solitude, reading a book or browse the internet on your laptop.</p> 
