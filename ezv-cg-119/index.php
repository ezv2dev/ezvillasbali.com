		<p>This stylish Zen-inspired holiday retreat is home to 8 bedroom suites, 4 located inside the main building and the other 4 in a separate area. All bedrooms are equipped with air-conditioning and adjoined by a private en-suite bathroom. 6 of the bedrooms are fitted with king-size beds, 1 with twin beds and 1 with 2 bunk-beds for 4 kids or 2 adults.</p> 
